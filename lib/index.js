/**
 * 是否是节假日
 * m: moment支持的时间格式
 */
function isChineseHoliday(m) {
  m = moment(m)

  const day = m.day()
  const date = m.format('YYYY-MM-DD')

  // 周末周六
  if (day === 0 || day === 6) {
    return !workdays[date]
  } else {
    return !!holidays[date]
  }
}

